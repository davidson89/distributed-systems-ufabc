private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
    String conteudo = this.txtConteudo.getText();
    String nome = this.txtNome.getText();

    if (conteudo.isEmpty() || nome.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Deve preencher campos nome e conteudo!");
        return;
    }
    if (this.cliente.armazena(nome, conteudo)) {
        JOptionPane.showMessageDialog(null, "Arquivo armazenado com sucesso");
    }

}//GEN-LAST:event_jButton5ActionPerformed

