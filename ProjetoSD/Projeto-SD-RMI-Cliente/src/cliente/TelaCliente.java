private void btnSalvarDiscoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarDiscoActionPerformed
   
   JFileChooser fileChooser = new JFileChooser();
   fileChooser.setDialogTitle("Specify a file to save");    
   int userSelection = fileChooser.showSaveDialog(this);
 
if (userSelection == JFileChooser.APPROVE_OPTION) {
       
         File arquivo   = fileChooser.getSelectedFile();   
         FileOutputStream fos; 
         String texto = this.txtConteudoRecuperado.getText();   
        try {
            fos = new FileOutputStream(arquivo);
            fos.write(texto.getBytes());    
            fos.close();
        } catch (IOException ex) {
            Logger.getLogger(TelaCliente.class.getName()).log(Level.SEVERE, null, ex);
        } ;
        }
}//GEN-LAST:event_btnSalvarDiscoActionPerformed

